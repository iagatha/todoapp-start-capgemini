    private void jTableTaskMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTaskMouseClicked
        int rowIndex = jTableTask.rowAtPoint(evt.getPoint().getLocation());
        int columnIndex = jTableTask.columnAtPoint(evt.getPoint().getLocation());
         Tasks tasks = taskModel.getTasks().get(rowIndex);
        switch (columnIndex) {
          
            case 3:
               taskController.update(tasks);
                break;
            case 4:
                
                break;
            case 5:
               
                taskController.removeById(tasks.getId());
                taskModel.getTasks().remove(tasks);
                int projectIndex = jListProjects.getSelectedIndex();
                Projects projects = (Projects) projectsModel.get(projectIndex);
                loadTasks(projects.getId());
                break;    
                
            default:
                throw new AssertionError();
        }
    }//GEN-LAST:event_jTableTaskMouseClicked

