    public void decorateTableTask(){
        
        //Customizando o Header da Table de Tarefas
        jTableTask.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        jTableTask.getTableHeader().setBackground(new Color(0,153,102));
        jTableTask.getTableHeader().setForeground(new Color(255, 255, 255));
        
        jTableTask.getColumnModel().getColumn(2).setCellRenderer(new DeadlineColumnCellRenderer());
        
        jTableTask.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCell("edit"));
        jTableTask.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCell("delete"));
        
        //Criando um sort automático para as colunas da Table
        jTableTask.setAutoCreateRowSorter(true);
    }
    
    // Inicialização do controller dentro do banco de dados
    public void initDataController(){
        projectController = new ProjectController();
        taskController = new TaskController();
    }
    
    //Carrega os objetos de projeto no banco
    public void initComponentModel(){
        projectsModel = new DefaultListModel();
        loadProjects();
        
        taskModel = new TaskTableModel();
        jTableTask.setModel(taskModel);
       
        
        if (!projectsModel.isEmpty()) {
            jListProjects.setSelectedIndex(0);
            int projectIndex = jListProjects.getSelectedIndex();
            Projects project = (Projects) projectsModel.get(projectIndex);
            loadTasks(project.getId());
        }
    }
    
    public void loadTasks(int idProjects){
        List<Tasks> tasks = taskController.getAll(idProjects);
        taskModel.setTasks(tasks);
        
        showJTableTasks(!tasks.isEmpty());
    }
    
    private void showJTableTasks(boolean hasTask){
        if(hasTask){
            if(jPanelEmptyList.isVisible()){
                jPanelEmptyList.setVisible(false);
                jPanel6.remove(jPanelEmptyList);
            }
            
            jPanel6.add(jScrollPane1);
            jScrollPane1.setVisible(true);
            jScrollPane1.setSize(jPanel6.getWidth(), jPanel6.getHeight());
        }else{
            if(jScrollPane1.isVisible()){
                jScrollPane1.setVisible(false);
                jPanel6.remove(jScrollPane1);
            }
            
            jPanel6.add(jPanelEmptyList);
            jPanelEmptyList.setVisible(true);
            jPanelEmptyList.setSize(jPanel6.getWidth(), jPanel6.getHeight());
        }
    }

    
    
    public void loadProjects(){
        List<Projects> projects = projectController.getAll();
        
        projectsModel.clear();
        
        for(int i = 0; i < projects.size(); i++){
            
            Projects project = projects.get(i);
            
            projectsModel.addElement(project);
        }
        
        jListProjects.setModel(projectsModel);
    }
